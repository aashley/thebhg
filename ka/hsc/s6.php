<p>The key point about Positions is that they are appointed or bestowed upon a Hunter. Positions designate a Hunter’s responsibility. For example, when a Hunter gets the position of Chief he or she is responsible for that respective Kabal; a Warden is responsible for his or her Wing; the Underlord is responsible for maintaining the roster; and so forth. To learn more about each individual position, visit the <a href="http://holonet.thebhg.org/index.php?module=library&page=book&id=1" target="_blank">Hunter’s Manual</a>.
